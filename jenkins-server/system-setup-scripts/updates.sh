update_system() {
  log "Updating system packages on $OS..."
  case "$PKG" in
    apt)
      sudo apt update -y || true
      sudo apt upgrade -y || {
        log "Retrying with --fix-missing..."
        sudo apt update -y --fix-missing
        sudo apt upgrade -y
      }
      ;;
    yum)
      sudo yum update -y
      ;;
    dnf)
      sudo dnf upgrade -y
      ;;
    *)
      err "Unsupported package manager: $PKG"
      ;;
  esac
}

