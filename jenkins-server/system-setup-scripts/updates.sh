detect_os() {
  [[ -f /etc/os-release ]] || err "/etc/os-release not found"
  source /etc/os-release

  case "$ID" in
    ubuntu)
      OS="ubuntu"
      PKG="apt"
      ;;
    amzn)
      [[ "$VERSION_ID" =~ ^2 ]] && OS="amazon2" || OS="amazon2023"
      PKG="yum"
      ;;
    centos|rhel)
      OS="$ID"
      PKG="yum"
      ;;
    *)
      err "Unsupported OS: $ID"
      ;;
  esac
}

